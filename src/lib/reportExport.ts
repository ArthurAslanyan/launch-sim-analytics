const cellBorder = { style: BorderStyle.SINGLE, size: 4, color: "CCCCCC" };
const cellBorders = {
  top: cellBorder,
  bottom: cellBorder,
  left: cellBorder,
  right: cellBorder,
};

function labelCell(label: string, value: string): TableRow {
  return new TableRow({
    children: [
      new TableCell({
        borders: cellBorders,
        width: { size: 35, type: WidthType.PERCENTAGE },
        children: [new Paragraph({ children: [new TextRun({ text: label, bold: true })] })],
      }),
      new TableCell({
        borders: cellBorders,
        width: { size: 65, type: WidthType.PERCENTAGE },
        children: [new Paragraph({ children: [new TextRun(value)] })],
      }),
    ],
  });
}

export async function exportToDOCX(
  game: GameConcept,
  results: SimulationResults,
  options: ExportOptions,
): Promise<void> {
  const children: (Paragraph | Table)[] = [];

  children.push(
    new Paragraph({
      text: game.gameName || "Untitled Game",
      heading: HeadingLevel.TITLE,
      alignment: AlignmentType.CENTER,
      spacing: { after: 200 },
    }),
    new Paragraph({
      text: "Game Concept Simulation Report",
      alignment: AlignmentType.CENTER,
      spacing: { after: 400 },
    }),
    new Paragraph({
      text: `Report Date: ${new Date().toLocaleDateString("en-GB", {
        day: "numeric",
        month: "long",
        year: "numeric",
      })}`,
      spacing: { after: 400 },
    }),
  );

  if (options.sections.performanceScore) {
    children.push(
      new Paragraph({
        text: "Executive Summary",
        heading: HeadingLevel.HEADING_1,
        spacing: { before: 400, after: 200 },
      }),
      new Paragraph({
        children: [
          new TextRun({ text: "Overall Performance Score: ", bold: true }),
          new TextRun({
            text: `${results.performanceScore.overall.toFixed(1)} / 10`,
            size: 28,
          }),
        ],
        spacing: { after: 120 },
      }),
      new Paragraph({
        children: [
          new TextRun({ text: "Session Quality: ", bold: true }),
          new TextRun(`${results.performanceScore.sessionQuality.toFixed(1)}`),
        ],
      }),
      new Paragraph({
        children: [
          new TextRun({ text: "Player Retention: ", bold: true }),
          new TextRun(`${results.performanceScore.playerRetention.toFixed(1)}`),
        ],
      }),
      new Paragraph({
        children: [
          new TextRun({ text: "Feature Efficiency: ", bold: true }),
          new TextRun(`${results.performanceScore.featureEfficiency.toFixed(1)}`),
        ],
      }),
      new Paragraph({
        children: [
          new TextRun({ text: "Market Fit: ", bold: true }),
          new TextRun(`${results.performanceScore.marketFit.toFixed(1)}`),
        ],
        spacing: { after: 200 },
      }),
    );
  }

  if (options.sections.recommendation) {
    children.push(
      new Paragraph({
        text: "Recommendation",
        heading: HeadingLevel.HEADING_2,
        spacing: { before: 300, after: 120 },
      }),
      new Paragraph({
        children: [new TextRun(results.recommendation)],
        spacing: { after: 300 },
      }),
    );
  }

  if (options.sections.gameSummary) {
    children.push(
      new Paragraph({
        text: "Game Overview",
        heading: HeadingLevel.HEADING_1,
        spacing: { before: 400, after: 200 },
      }),
      new Table({
        width: { size: 100, type: WidthType.PERCENTAGE },
        rows: [
          labelCell("Game Type", game.gameType),
          labelCell("Grid Layout", game.gridLayout),
          labelCell("Volatility", game.volatility),
          labelCell("RTP Target", `${game.rtpTarget}%`),
          labelCell("Top Win", `${game.topWin}×`),
          labelCell("Target Markets", (game.targetMarkets || []).join(", ") || "—"),
        ],
      }),
      new Paragraph({ text: "", spacing: { after: 200 } }),
    );
  }

  if (options.sections.population) {
    children.push(
      new Paragraph({
        text: "Player Metrics",
        heading: HeadingLevel.HEADING_1,
        spacing: { before: 400, after: 200 },
      }),
      new Table({
        width: { size: 100, type: WidthType.PERCENTAGE },
        rows: [
          labelCell("Day 1 Retention", `${results.simulatedPopulation.retentionD1}%`),
          labelCell("Day 7 Retention", `${results.simulatedPopulation.retentionD7}%`),
          labelCell("Churn Rate", `${results.simulatedPopulation.churnRate}%`),
          labelCell(
            "Avg Session Duration",
            `${results.simulatedPopulation.avgSessionDurationMinutes} min`,
          ),
          labelCell(
            "Avg Rounds per Session",
            `${results.simulatedPopulation.avgRoundsPerSession}`,
          ),
        ],
      }),
      new Paragraph({ text: "", spacing: { after: 200 } }),
    );
  }

  if (options.sections.archetypeFit && results.archetypeFitScores?.length) {
    children.push(
      new Paragraph({
        text: "Archetype Fit Analysis",
        heading: HeadingLevel.HEADING_1,
        spacing: { before: 400, after: 200 },
      }),
    );
    results.archetypeFitScores.forEach((a) => {
      children.push(
        new Paragraph({
          children: [
            new TextRun({ text: `${a.archetype}: `, bold: true }),
            new TextRun(`${a.finalScore.toFixed(1)} — ${a.fitLabel}`),
          ],
        }),
      );
    });
    children.push(new Paragraph({ text: "", spacing: { after: 200 } }));
  }

  if (options.sections.dataInterpretation && results.dataInterpretation?.length) {
    children.push(
      new Paragraph({
        text: "Key Insights",
        heading: HeadingLevel.HEADING_1,
        spacing: { before: 400, after: 200 },
      }),
    );
    results.dataInterpretation.forEach((insight, idx) => {
      children.push(
        new Paragraph({
          children: [
            new TextRun({ text: `${idx + 1}. ${insight.category}`, bold: true }),
          ],
          spacing: { before: 120, after: 60 },
        }),
        new Paragraph({
          children: [new TextRun(insight.narrative || "")],
          spacing: { after: 80 },
        }),
      );
    });
  }

  children.push(
    new Paragraph({
      text: `Generated by LaunchIndex • ${new Date().toLocaleDateString()}`,
      alignment: AlignmentType.CENTER,
      spacing: { before: 600 },
    }),
  );

  const doc = new Document({ sections: [{ children }] });
  const blob = await Packer.toBlob(doc);
  const url = URL.createObjectURL(blob);
  const a = document.createElement("a");
  a.href = url;
  a.download = `${options.filename}.docx`;
  document.body.appendChild(a);
  a.click();
  document.body.removeChild(a);
  URL.revokeObjectURL(url);
}
