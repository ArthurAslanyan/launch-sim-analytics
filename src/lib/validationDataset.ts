// ── Validation-specific metadata (not part of GameConcept) ──

export interface KnownOutcomes {
  // Market reality
  marketPresence: "High" | "Medium" | "Low";
  targetMarkets: string[];
  estimatedSessionLengthMinutes: number;   // typical player session
  sessionProfile: "Short" | "Medium" | "Long";

  // Player behaviour (known from industry data / player communities)
  primaryArchetype: string;       // dominant player type attracted
  secondaryArchetype: string;     // secondary player type
  archetypeReason: string;        // why this archetype dominates

  // Accuracy tracking fields
  launchIndexPredictedArchetype?: string;    // filled in after running simulation
  archetypePredictionCorrect?: boolean;      // filled in after comparison
  sessionPredictionDeltaMinutes?: number;    // predicted - actual

  // Context
  knownStrengths: string[];        // what the game does well commercially
  knownWeaknesses: string[];       // documented commercial weaknesses
  notes: string;
}

export interface ValidationEntry {
  // Identity
  gameName: string;
  provider: string;
  releaseYear: number;

  // Full GameConcept — all fields that match the LaunchIndex form
  gameConcept: GameConcept;

  // What we know happened in the market
  knownOutcomes: KnownOutcomes;
}

// ── Helpers ──────────────────────────────────────────────────

function makeFeature(
  name: string,
  type: string,
  triggerFrequency: string,
  averageValue: number,
  maxValue: number,
  featureVolatility: "Low" | "Medium" | "High"
): Feature {
  return {
    id: name.toLowerCase().replace(/\s+/g, "-"),
    type,
    name,
    triggerCondition: "3+ Scatter symbols",
    triggerFrequency,
    averageValue,
    maxValue,
    featureVolatility,
    visibility: "",
    winImpact: "",
    progressImpact: "",
  };
}

