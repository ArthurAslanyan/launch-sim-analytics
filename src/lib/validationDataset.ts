/**
 * Returns entries grouped by archetype for accuracy analysis.
 */
export function getEntriesByArchetype(archetype: string): ValidationEntry[] {
  return VALIDATION_DATASET.filter(
    e => e.knownOutcomes.primaryArchetype === archetype ||
         e.knownOutcomes.secondaryArchetype === archetype
  );
}

/**
 * Returns entries grouped by volatility for model calibration.
 */
export function getEntriesByVolatility(volatility: string): ValidationEntry[] {
  return VALIDATION_DATASET.filter(e => e.gameConcept.volatility === volatility);
}

/**
 * Returns entries that require simulation (for flagging in validation runner).
 */
export function getSimulationRequiredEntries(): ValidationEntry[] {
  return VALIDATION_DATASET.filter(e => e.gameConcept.requiresSimulation === true);
}

/**
 * Summary statistics for quick dataset inspection.
 */
export function getDatasetSummary() {
  const byVolatility = {
    Low: VALIDATION_DATASET.filter(e => e.gameConcept.volatility === "Low").length,
    Medium: VALIDATION_DATASET.filter(e => e.gameConcept.volatility === "Medium").length,
    High: VALIDATION_DATASET.filter(e => e.gameConcept.volatility === "High").length,
    "Very High": VALIDATION_DATASET.filter(e => e.gameConcept.volatility === "Very High").length,
  };

  const byGameType = VALIDATION_DATASET.reduce<Record<string, number>>((acc, e) => {
    acc[e.gameConcept.gameType] = (acc[e.gameConcept.gameType] || 0) + 1;
    return acc;
  }, {});

  const avgSessionLength =
    VALIDATION_DATASET.reduce((s, e) => s + e.knownOutcomes.estimatedSessionLengthMinutes, 0) /
    VALIDATION_DATASET.length;

  return {
    totalGames: VALIDATION_DATASET.length,
    byVolatility,
    byGameType,
    avgSessionLengthMinutes: Math.round(avgSessionLength * 10) / 10,
    requiresSimulation: VALIDATION_DATASET.filter(e => e.gameConcept.requiresSimulation).length,
  };
}
